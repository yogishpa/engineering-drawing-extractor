def display_confidence_badge(confidence: float) -> str:
    """Create a colored confidence badge based on confidence level."""
    if confidence >= 0.8:
        return f"üü¢ **{confidence:.1%}** (High)"
    elif confidence >= 0.5:
        return f"üü° **{confidence:.1%}** (Medium)"
    else:
        return f"üî¥ **{confidence:.1%}** (Low)"


